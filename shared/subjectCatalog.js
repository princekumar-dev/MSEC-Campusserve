export const normalizeDepartment = (department = '') => (
  department.toString().trim().toUpperCase().replace(/[\s-]+/g, '_')
)

export const normalizeYear = (year = '') => {
  const key = year.toString().trim().toUpperCase()
  return YEAR_ALIASES[key] || key
}

export const normalizeSemester = (semester = '') => {
  const key = semester.toString().trim().toUpperCase()
  return SEMESTER_ALIASES[key] || key
}

export const normalizeSubjectKey = (value = '') => (
  value
    .toString()
    .trim()
    .toLowerCase()
    .replace(CODE_PATTERN, '')
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '')
)

export const formatSubjectLabel = ({ code, name } = {}) => {
  const cleanCode = code ? code.toString().trim().toUpperCase() : ''
  const cleanName = name ? name.toString().trim() : ''
  if (cleanCode && cleanName) return `${cleanCode} - ${cleanName}`
  return cleanName || cleanCode
}

export const splitSubjectLabel = (label = '') => {
  const raw = label.toString().trim()
  if (!raw) return { code: '', name: '' }

  const slashParts = raw.split('/').map((part) => part.trim()).filter(Boolean)
  if (slashParts.length === 2) {
    const firstCode = slashParts[0].match(CODE_PATTERN)?.[0]
    const secondCode = slashParts[1].match(CODE_PATTERN)?.[0]
    if (firstCode || secondCode) {
      const code = (firstCode || secondCode).toUpperCase()
      const name = firstCode ? slashParts[1] : slashParts[0]
      return { code, name: name.trim() }
    }
  }

  const code = raw.match(CODE_PATTERN)?.[0]?.toUpperCase() || ''
  const name = code
    ? raw
        .replace(CODE_PATTERN, '')
        .replace(/^\s*[-:/|]+\s*/, '')
        .replace(/\s*[-:/|]+\s*$/, '')
        .trim()
    : raw

  return { code, name }
}

export const getDefaultSubjects = (department, year, semester) => {
  const deptKey = normalizeDepartment(department)
  const yearKey = normalizeYear(year)
  const semesterKey = normalizeSemester(semester)
  return DEFAULT_SUBJECTS[deptKey]?.[yearKey]?.[semesterKey] || []
}

export const normalizeSubject = (subjectName, department, year, semester) => {
  const parsed = splitSubjectLabel(subjectName)
  const defaults = getDefaultSubjects(department, year, semester)
  const matchedDefault = defaults.find((subject) => {
    if (parsed.code && subject.code?.toUpperCase() === parsed.code) return true
    return normalizeSubjectKey(subject.name) === normalizeSubjectKey(parsed.name)
  })

  const code = parsed.code || matchedDefault?.code || ''
  const name = parsed.name || matchedDefault?.name || subjectName.toString().trim()

  return {
    subjectCode: code ? code.toUpperCase() : '',
    subjectName: formatSubjectLabel({ code, name })
  }
}
